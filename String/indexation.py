# Формат выходных данных
# Программа должна вывести в столбик элементы строки в обратном порядке.

string = input()
for index in range(len(string) - 1, -1, -1):
    print(string[index])

# or
string = input()
for index in reversed(range(0, len(string))):
    print(string[index])


