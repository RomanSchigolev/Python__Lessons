# 1. На вход программе подается одна строка.
# Напишите программу, которая выводит элементы строки с индексами 0, 2, 4, ... в столбик.

# Формат входных данных
# На вход программе подается одна строка.

# Формат выходных данных
# Программа должна вывести элементы строки с индексами 0, 2, 4, ..., каждое на отдельной строке.

string = input()
for index in range(len(string)):
    if index % 2 == 0:
        print(string[index])

# or
string = input()
for index in range(0, len(string), 2):
    print(string[index])


# 2. На вход программе подается одна строка.
# Напишите программу, которая выводит в столбик элементы строки в обратном порядке.

# Формат входных данных
