# Формат входных данных
# На вход программе подаётся натуральное число – количество собачьих лет.

# Формат выходных данных
# Программа должна вывести возраст собаки в человеческих годах.

# Примечание. В течение первых двух лет собачий год равен 10.5 человеческим годам.
# После этого каждый год собаки равен 4 человеческим годам.

dog_age = float(input())
first_years = 10.5
other_years = 4
people_age = 0
remains = 0

if dog_age == 1:
    people_age = first_years
    print(people_age)
elif dog_age == 2:
    people_age = first_years * 2
    print(people_age)
elif dog_age > 2:
    remains = dog_age - 2
    people_age = first_years * 2 + remains * other_years
    print(people_age)


