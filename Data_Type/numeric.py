# 9. Напишите программу, которая упорядочивает три числа от большего к меньшему.

# Формат входных данных
# На вход программе подается три целых числа, каждое на отдельной строке.

# Формат выходных данных
# Программа должна вывести три числа, каждое на отдельной строке, упорядоченных от большего к меньшему.

a, b, c = [int(input()) for _ in range(3)]
most = max(a, b, c)
least = min(a, b, c)
middle = sum([a, b, c]) - most - least
print(most, middle, least, sep="\n")


# 10. Назовем число интересным, если в нем разность максимальной и минимальной цифры равняется средней по величине цифре.
# Напишите программу, которая определяет интересное число или нет.
# Если число интересное, следует вывести – «Число интересное» иначе «Число неинтересное».

# Формат входных данных
# На вход программе подается целое трехзначное число.

# Формат выходных данных
# Программа должна вывести текст в соответствии с условием задачи.

number = int(input())

units = number % 10
tens = number // 10 % 10
hundreds = number // 100

most = max(hundreds, tens, units)
least = min(hundreds, tens, units)
middle = sum([hundreds, tens, units]) - most - least

if most - least == middle:
    print("Число интересное")
else:
    print("Число неинтересное")


