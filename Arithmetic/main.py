# 6. Напишите программу, которая считает стоимость трех компьютеров, состоящих из монитора, системного блока, клавиатуры и мыши.

# Формат входных данных
# На вход программе подаётся четыре целых числа, каждое на отдельной строке.
# В первой строке — стоимость монитора, во второй строке — стоимость системного блока, в третье строке — стоимость клавиатуры и в четвертой строке — стоимость мыши.

# Формат выходных данных
# Программа должна вывести одно число – стоимость покупки (трех компьютеров).

monitor, system_unit, keyboard, mouse = [int(input()) for _ in range(4)]
one_computer_price = monitor + system_unit + keyboard + mouse
print(one_computer_price * 3)

# or

print(sum(int(input()) for _ in range(4)) * 3)


