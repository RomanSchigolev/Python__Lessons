# Формат выходных данных
# Программа должна вывести количество одинаковых соседних символов.

string = input()
counter = 0
for i in range(len(string) - 1):
    if string[i] == string[i + 1]:
        counter = counter + 1
    else:
        continue
print(counter)


# 8. На вход программе подается одна строка с буквами русского языка.
# Напишите программу, которая определяет количество гласных и согласных букв.

# Формат входных данных
# На вход программе подается одна строка.

# Формат выходных данных
# Программа должна вывести количество гласных и согласных букв.

# Примечание. В русском языке 10 гласных букв (а, у, о, ы, и, э, я, ю, ё, е) и
# 21 согласная буква (б, в, г, д, ж, з, й, к, л, м, н, п, р, с, т, ф, х, ц, ч, ш, щ).

string = str(input()).lower()

vowel_letters = ["а", "у", "о", "ы", "и", "э", "я", "ю", "е", "ё"]
consonant_letters = ["б", "в", "г", "д", "ж", "з", "й", "к", "л", "м", "н", "п", "р", "с", "т", "ф", "х", "ц", "ч", "ш", "щ"]

vowels_count = 0
consonants_count = 0

for letter in string:
    if letter in vowel_letters:
        vowels_count = vowels_count + 1
    elif letter in consonant_letters:
        consonants_count = consonants_count + 1
    else:
        continue
print(f"Количество гласных букв равно {vowels_count}")
print(f"Количество согласных букв равно {consonants_count}")


# 9. На вход программе подается натуральное число, записанное в десятичной системе счисления.
# Напишите программу, которая переводит данное число в двоичную систему счисления.

# Формат входных данных
# На вход программе подается одно натуральное число строка.

# Формат выходных данных
# Программа должна вывести число записанное в двоичной системе счисления.

number = int(input())
b = ""
while number != 0:
    b = str(number % 2) + b
    number = number // 2
print(b)
